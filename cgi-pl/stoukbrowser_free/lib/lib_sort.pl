# Library to handle sorting

##############################################################################
sub lib_sort_by_number_asc {
# ----------------------------------------------------------------------------
    if ($a < $b) {
        return -1;
    } elsif ($a == $b) {
        return 0;
    } elsif ($a > $b) {
        return 1;
    }
}
